<br><b>Web Application for Analyzing the Stock Market through Social Media</b>
<br>June 2015 - Present
    <br>
<br>I am currently developing browser application that searches for patterns in social media keywords during significant
    changes in the U.S. stock market. I have written Python scripts to parse Yahoo! Finance historical stock prices
    and graphs for companies in Dow Jones Industrial as well as retrieve statuses from Twitter through its REST API. 

    <br><br>The objective is to determine whether there is a correlation between significant changes in the U.S. stock market
    and phrases that are posted on social media. Different algorithms will be implemented for word associations for increases
    and decreases in the stock market.
    
    <br><br>The application is curently under construction at:
    <br><a href="http://finance.eddylu.com/">finance.eddylu.com/</a>
    
    <br><br>
    
    Languages:&ensp;Python, HTML, CSS, JavaScript, jQuery
